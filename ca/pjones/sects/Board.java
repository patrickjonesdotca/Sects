package ca.pjones.sects;

public class Board {
	
	private int xFields;
	private int yFields;

	public Board() {
		
	}

	public int getxFields() {
		return xFields;
	}

	public void setxFields(int xFields) {
		this.xFields = xFields;
	}

	public int getyFields() {
		return yFields;
	}

	public void setyFields(int yFields) {
		this.yFields = yFields;
	}
}
